export const applyTheme = (term, themeName) => {
  const theme = themes[themeName] || themes.default;

  // Update terminal colors
  term.options.theme = theme;

  // Update page background
  document.body.style.backgroundColor = theme.background;
  document.getElementById("__next").style.backgroundColor = theme.background;

  // Update scrollbar styles
  if (theme != themes.default) {
    const style = document.createElement("style");
    style.textContent = `
      .xterm .xterm-viewport::-webkit-scrollbar-track {
        background: ${theme.background} !important;
      }
      .xterm .xterm-viewport::-webkit-scrollbar-thumb {
        background: ${theme.foreground} !important;
      }
      ::-webkit-scrollbar {
        scrollbar-color: ${theme.foreground} ${theme.background} !important;
      }
    `;

    // Remove any existing theme styles
    const existingStyle = document.querySelector("style[data-theme-style]");
    if (existingStyle) {
      existingStyle.remove();
    }
    style.setAttribute("data-theme-style", "true");
    document.head.appendChild(style);
  }

  return true;
};
